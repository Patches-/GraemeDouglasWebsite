        <p>My name is Graeme Douglas; born in Glasgow in 1982 I’ve been working as a
        designer for the last fourteen years. I began my career at eighteen whilst studying
        furniture design and construction at college. Since then I’ve been working to
        develop my construction skills and also a design process that allows me to work
        more eusciently. After graduating from University with an honours degree in product
        design, it was clear I required some experience within the business world. I chose to
        work within the creative social enterprise within the east end of Glasgow. It was here
        that I really began to develop what I had learned over the years, the process has in no
        way been quick, or easy, however my creative approach to project development and
        delivery has led to me being highly commended at the inaugural Craft Skills Awards
        in 2013.</p>
        <br>
        <p>My experiences have been key in my development, it was this thought that led to the
        development of the creative program I am currently working on. Developing
        creative skills within people is impossible without giving them the opportunity to
        experience success, failure, new settings and situations or just the time to reect on
        their skills and future ambitions. I believe that as human beings we are all naturally
        creative, we just need time to appreciate that we are being creative or a nudge to say
        it’s not childish to sit and day dream for thirty minutes!</p>
