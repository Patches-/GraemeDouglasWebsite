        <div class="title-font">Education &amp; Experience</div>
        <div class="line"></div>
        <div id="ed" class="maintext">
            <p>Working as a cabinet maker really pushed my hand skills and
            commitment to work, It was a steep learning curve as the
            workload was high and the standard of finish was almost glass
            like in definition. It was during this time that I began to be
            obsessive; some might say obsessive and compulsive about
            striving for perfection. Unhappy with the absolute lack of
            cohesion between the design and cabinet workshop I left the
            industry to obtain an honours degree in product design to give
            myself the broadest and most comprehensive approach to
            design and manufacture.</p>
            <br>
            <p>My work with Impact Arts has been a defining moment within my
            career. Working on the Creative Pathways program has given me
            the scope to create an approach to delivering workshops and
            projects that enhance the users divergent thinking processes.
            The program is constantly developing and in 2013 I wrote four
            SQA accredited units for delivery over the eighteen week
            program. The young designers have also had the opportunity to
            work with some high profile clients.</p>
            <br>
            <p>Design and creativity have been integral components in my
            success and something that keeps me delivering to a high
            standard. My approach to work can becoming consuming, a
            former design colleague once commented that we were
            Obsessive Compulsive Designers. I am not ashamed of this and
            in all honesty hope that there are more of us out there ready to
            shape the future in a positive and creative way!</p>

